first_name = "Himalaya"
last_name = "Kilaru"
age = 30

# My name is Himalaya Kilaru. I am 30 years old.

print('My name is ' + first_name + ' ' + last_name + '. I am ' + str(age) + ' years old.')  # Concatenation

# .format() method
print("My name is {} {}. I am {} years old.".format(first_name, last_name ,age))

# f string method
print(f"My name is {first_name} {last_name}. I am {age} years old.")






